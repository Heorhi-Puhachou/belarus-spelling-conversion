package by.spelling.conversion.util;

public class ReplacePair {
    private String narkam;
    private String tarask;

    public ReplacePair(String narkam, String tarask) {
        this.narkam = narkam;
        this.tarask = tarask;
    }

    public String getNarkam() {
        return narkam;
    }

    public String getTarask() {
        return tarask;
    }
}
